# Example 1: Looping through a range of numbers
for num in range(5):
    print(num)

# Output:
# 0
# 1
# 2
# 3
# 4

# Example 2: Looping through a range of numbers with a specified start and end
for num in range(2, 6):
    print(num)

# Output:
# 2
# 3
# 4
# 5

# Example 3: Looping through a range of numbers with a specified start, end, and step
for num in range(1, 10, 2):
    print(num)

# Output:
# 1
# 3
# 5
# 7
# 9
